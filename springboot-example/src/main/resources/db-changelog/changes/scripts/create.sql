CREATE TABLE CUSTOMERS (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(252),
    CREATE_DATE TIMESTAMP,
    PRIMARY KEY (ID)
);

CREATE TABLE ORDERS (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    CUSTOMER_ID BIGINT,
    PRODUCT_NAME VARCHAR(128),
    PRICE BIGINT,
    PRIMARY KEY (ID),
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS(ID)
);

CREATE TABLE CAR (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    MODEL_NAME VARCHAR(252),
    MODEL_YEAR BIGINT,
    CREATE_DATE TIMESTAMP,
    PRIMARY KEY (ID)
);

CREATE TABLE TODOS (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    TITLE VARCHAR(128),
    DESCRIPTION VARCHAR(128),
    DELETED NUMBER(1),
    PRIMARY KEY (ID)
);

CREATE TABLE PERSONS (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    FIRSTNAME VARCHAR(128),
    LASTNAME VARCHAR(128),
    AGE NUMBER(11),
    DELETED NUMBER(1),
    PRIMARY KEY (ID)
);

CREATE TABLE NOTIFICATION (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    FIRST_NAME VARCHAR(25),
    LAST_NAME VARCHAR(25),
    CONTENT VARCHAR(25),
    PRIMARY KEY (ID)
);

CREATE TABLE SMS_NOTIFICATION (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    PHONE_NUMBER VARCHAR(25),
    PRIMARY KEY (ID)
);

CREATE TABLE EMAIL_NOTIFICATION (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    EMAIL VARCHAR(100),
    PRIMARY KEY (ID)
);

CREATE TABLE EMPLOYEE (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    FIRST_NAME VARCHAR(25),
    LAST_NAME VARCHAR(25),
    HOURLY_RATE BIGINT,
    SALARY BIGINT,
    DTYPE VARCHAR(25),
    PRIMARY KEY (ID)
);

CREATE TABLE GAME (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(25),
    DESCRIPTION VARCHAR(255),
    ROUND_NO BIGINT,
    SESSION_ID BIGINT,
    LEAGUE_ID BIGINT,
    POSTPONED NUMBER(1),
    PLAYED NUMBER(1),
    CREATE_DATE TIMESTAMP,
    PRIMARY KEY (ID)
);

CREATE TABLE PAYMENT (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    AMOUNT BIGINT,
    CARD_NO BIGINT,
    CHEQUE_NO BIGINT,
    PRIMARY KEY (ID)
);

CREATE TABLE CARD_PAYMENT (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    AMOUNT BIGINT,
    CARD_NO BIGINT,
    CHEQUE_NO BIGINT,
    PRIMARY KEY (ID)
);

CREATE TABLE CHEQUE_PAYMENT (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    AMOUNT BIGINT,
    CARD_NO BIGINT,
    CHEQUE_NO BIGINT,
    PRIMARY KEY (ID)
);

CREATE TABLE NEWS (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    TITLE VARCHAR(128),
    CATEGORY_NAME VARCHAR(128),
    DESCRIPTION VARCHAR(128),
    DELETED NUMBER(1) DEFAULT 0,
    PRIMARY KEY (ID)
);

CREATE TABLE TBL_POST (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    TITLE VARCHAR(128),
    DESCRIPTION VARCHAR(128),
    PRIMARY KEY (ID)
);

CREATE TABLE TBL_TAGS (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(128),
    PRIMARY KEY (ID)
);

CREATE TABLE TBL_POST_TAGS (
    POST_ID BIGINT NOT NULL,
    TAG_ID BIGINT NOT NULL,
    PRIMARY KEY (POST_ID,TAG_ID)
);


CREATE TABLE TBL_CATEGORIES (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(128),
    DESCRIPTION VARCHAR(128),
    PRIMARY KEY (ID)
);

CREATE TABLE TBL_PRODUCT (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(128),
    PRICE BIGINT,
    PRIMARY KEY (ID)
);

CREATE TABLE TBL_CATEGORY_PRODUCT (
    CATEGORY_ID BIGINT NOT NULL,
    PRODUCT_ID BIGINT NOT NULL,
    PRIMARY KEY (CATEGORY_ID,PRODUCT_ID),
    CONSTRAINT PRODUCT_ID_FK FOREIGN KEY (PRODUCT_ID) REFERENCES TBL_PRODUCT(ID),
    CONSTRAINT CATEGORY_ID_FK FOREIGN KEY (CATEGORY_ID) REFERENCES TBL_CATEGORIES(ID)
);

CREATE TABLE TBL_CAMPUS (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(128),
    WEB_SITE_URL VARCHAR(128),
    PHONE VARCHAR(128),
    EMAIL VARCHAR(128),
    PRIMARY KEY (ID)
);

CREATE TABLE TBL_CLASSROOM (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    CAMPUS_ID BIGINT,
    NAME VARCHAR(128),
    NO VARCHAR(128),
    PRIMARY KEY (ID),
    CONSTRAINT CAMPUS_ID_FK FOREIGN KEY (CAMPUS_ID) REFERENCES TBL_CAMPUS(ID)
);

CREATE TABLE TBL_SYS_USER (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    USERNAME VARCHAR(128),
    PASSWORD VARCHAR(128),
    FULLNAME VARCHAR(128),
    EMAIL VARCHAR(128),
    PHONE VARCHAR(128),
    IS_ACTIVE NUMBER(1),
    CREATED_DATE TIMESTAMP ,
    PRIMARY KEY (ID)
);

CREATE TABLE TBL_PRODUCT_SHIPMENT (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    PRODUCT_ID BIGINT ,
    CARGO_FOLLOW_URL VARCHAR(128),
    IS_SELLER_FEE NUMBER(1),
    PRIMARY KEY (ID)
);

CREATE TABLE TBL_DELIVERY_RECEIPT (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    PRODUCT_SHIPMENT_ID BIGINT,
    RECEIPT_NUMBER VARCHAR(128),
    RECEIVER VARCHAR(128),
    DELIVERY_DATE TIMESTAMP,
    PRIMARY KEY (ID)
);

CREATE TABLE TBL_MEETING (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(128),
    START_DATE TIMESTAMP,
    PRIMARY KEY (ID)
);

CREATE TABLE TBL_MEMBER (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    FULL_NAME VARCHAR(128),
    PRIMARY KEY (ID)
);

CREATE TABLE TBL_MEETING_MEMBER (
    MEETING_ID BIGINT NOT NULL,
    MEMBER_ID BIGINT NOT NULL,
    PRIMARY KEY (MEETING_ID,MEMBER_ID),
    CONSTRAINT MEMBER_ID_FK FOREIGN KEY (MEMBER_ID) REFERENCES TBL_MEMBER(ID),
    CONSTRAINT MEETING_ID_FK FOREIGN KEY (MEETING_ID) REFERENCES TBL_MEETING(ID)
);

CREATE TABLE SALARY (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    FULLNAME VARCHAR(128),
    EMAIL VARCHAR(128),
    PHONE VARCHAR(128),
    CITY VARCHAR(128),
    SALARY BIGINT NOT NULL,
    SDATE TIMESTAMP,
    PRIMARY KEY (ID)
);
